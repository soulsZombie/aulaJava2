package entidade;

public  class conta {

	
	private int nunConta;
	private double saldo;
	private double sacaValor;
	
	
	public int getNunConta() {
		return nunConta;
	}
	public void setNunConta(int nunConta) {
		this.nunConta = nunConta;
	}
	
	
	public double getSaldo() {
		return saldo;
	}
	public void setSaldo(double saldo) {
		this.saldo = saldo;
	}
	
	

public double getSacaValor() {
	return sacaValor;
}
public void setSacaValor(double sacaValor) {
	this.sacaValor = sacaValor;


}
		
	
}
